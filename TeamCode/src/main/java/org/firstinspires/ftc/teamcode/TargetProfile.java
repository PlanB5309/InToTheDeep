package org.firstinspires.ftc.teamcode;

public class TargetProfile {
    double maxSpeed;
    double minSpeed;
    double maxDistance;
    double maxAngle;
    double startSlowDown;

    public TargetProfile(double maxSpeed, double minSpeed, double maxDistance, double maxAngle, double startSlowDown){
        this.maxSpeed = maxSpeed;
        this.minSpeed = minSpeed;
        this.maxDistance = maxDistance;
        this.maxAngle = maxAngle;
        this.startSlowDown = startSlowDown;
    }


}
